	-- Reference to the WelcomeGui and TextLabel
	local welcomeGui = playerGui:WaitForChild("ScreenGui") -- Assuming "ScreenGui" is the name of your ScreenGui
	local textLabel = welcomeGui:WaitForChild("Welcome") -- Assuming "Welcome" is the name of your TextLabel
	
	-- Function to show the welcome notification with animation
	local function showWelcomeNotification()
		textLabel.Text = "Welcome to Milenio X hub!, " .. player.Name .. "!"
	
		-- Initial properties before animation
		textLabel.Visible = true
		textLabel.Position = UDim2.new(0.5, 0, -0.2, 0)  -- Start above the screen
	
		-- Tween animation to bring the notification down smoothly
		local tweenInfo = TweenInfo.new(1, Enum.EasingStyle.Quad, Enum.EasingDirection.Out)
		local tweenDown = game.TweenService:Create(textLabel, tweenInfo, { Position = UDim2.new(0.5, 0, 0.2, 0) })
		tweenDown:Play()
	
		-- Tween animation to fade out and move back up after 3 seconds
		wait(3)
		local tweenUp = game.TweenService:Create(textLabel, TweenInfo.new(1, Enum.EasingStyle.Quad, Enum.EasingDirection.In), { Position = UDim2.new(0.5, 0, -0.2, 0), TextTransparency = 1 })
		tweenUp:Play()
	
		-- Hide the TextLabel after animation completes
		tweenUp.Completed:Connect(function()
			textLabel.Visible = false
			textLabel.Position = UDim2.new(0.5, 0, -0.2, 0)  -- Reset position for next use
			textLabel.TextTransparency = 0  -- Reset transparency for next use
		end)
	end
	
	-- Show the welcome notification when the player joins
	showWelcomeNotification()
